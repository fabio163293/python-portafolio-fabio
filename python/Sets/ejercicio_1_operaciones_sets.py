set_a = {1,2,3,4}
set_b = {3,4,5,6}


# | une los elementos de dos set
print(
 set_a | set_b
  )

# & devuelve los elementos comunes entre los set
print(
    set_a & set_b
)

# - devuelve los elementos del set primero que no esten en el secundario
print(
    set_a - set_b
)

# ^ devuelve los elementos diferentes en ambos set
print(
    set_a ^ set_b 
) 
 